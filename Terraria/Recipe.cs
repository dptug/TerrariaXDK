	public void Create(UI ui)
	{
		for (int i = 0; i < numRequiredItems; i++)
		{
			int num = requiredItem[i].stack;
			for (int j = 0; j < 48; j++)
			{
				if (ui.player.inventory[j].netID == requiredItem[i].netID)
				{
					if (ui.player.inventory[j].stack > num)
					{
						ui.player.inventory[j].stack -= (short)num;
						num = 0;
					}
					else
					{
						num -= ui.player.inventory[j].stack;
						ui.player.inventory[j].Init();
					}
				}
				if (num <= 0)
				{
					break;
				}
			}
		}
		if ((requiredTile[0] == 16 || requiredTile[0] == 134) && ++ui.totalAnvilCrafting == 300)
		{
			ui.AchievementTriggers.SetState(Trigger.UsedLotsOfAnvils, state: true);
		}
		switch (category)
		{
		case Category.STRUCTURES:
			ui.Statistics.incStat(StatisticEntry.FurnitureCrafted);
			break;
		case Category.TOOLS:
			ui.Statistics.incStat(StatisticEntry.ToolsCrafted);
			break;
		case Category.WEAPONS:
			ui.Statistics.incStat(StatisticEntry.WeaponsCrafted);
			break;
		case Category.ARMOR:
			ui.Statistics.incStat(StatisticEntry.ArmorCrafted);
			break;
		case Category.POTIONS:
			ui.Statistics.incStat(StatisticEntry.ConsumablesCrafted);
			break;
		default:
			ui.Statistics.incStat(StatisticEntry.MiscCrafted);
			break;
		}
		switch (createItem.type)
		{
		case 8:
		case 427:
		case 428:
		case 429:
		case 430:
		case 431:
		case 432:
		case 433:
			ui.totalTorchesCrafted += (uint)createItem.stack;
			break;
		case 94:
			ui.totalWoodPlatformsCrafted += (uint)createItem.stack;
			break;
		case 26:
		case 30:
		case 93:
		case 479:
			ui.totalWallsCrafted += (uint)createItem.stack;
			break;
		case 19:
		case 20:
		case 21:
		case 22:
		case 57:
		case 117:
		case 175:
		case 381:
		case 382:
		case 391:
			ui.totalBarsCrafted += (uint)createItem.stack;
			if (ui.totalBarsCrafted >= 10000)
			{
				ui.player.AchievementTrigger(Trigger.CreatedLotsOfBars);
			}
			break;
		}
	}

	public static void FindRecipes(UI ui, Category cat, bool isCraftable)
	{
		int num = 0;
		int num2 = 1024;
		ui.player.UpdateRecipes();
		for (int i = 0; i < 342; i++)
		{
			Recipe recipe = Main.recipe[i];
			if (cat != recipe.category)
			{
				continue;
			}
			if (isCraftable)
			{
				if (!ui.player.CanCraftRecipe(recipe))
				{
					continue;
				}
			}
			else if (ui.craftGuide)
			{
				bool flag = false;
				for (int num3 = recipe.numRequiredItems - 1; num3 >= 0; num3--)
				{
					if (recipe.requiredItem[num3].netID == ui.guideItem.netID)
					{
						flag = true;
						break;
					}
				}
				if (!flag)
				{
					continue;
				}
			}
			else if (!ui.player.recipesFound[i])
			{
				continue;
			}
			int num4 = (int)recipe.subCategory;
			if (num4 == 0)
			{
				num++;
				num4 = num2++;
				currentRecipies.Add(num4, new SubCategoryList(1));
			}
			else if (!currentRecipies.ContainsKey(num4))
			{
				num++;
				currentRecipies.Add(num4, new SubCategoryList(32));
			}
			currentRecipies[num4].Add(ui.player, i);
		}
		Dictionary<int, SubCategoryList>.ValueCollection values = currentRecipies.Values;
		ui.currentRecipeCategory.Clear();
		foreach (SubCategoryList item in values)
		{
			if (item.canCraft)
			{
				ui.currentRecipeCategory.Add(item);
			}
		}
		foreach (SubCategoryList item2 in values)
		{
			if (!item2.canCraft)
			{
				ui.currentRecipeCategory.Add(item2);
			}
		}
		int count = currentRecipies.Count;
		if (count == 0)
		{
			ui.craftingRecipe = newRecipe;
			ui.craftingRecipeX = 0;
			ui.craftingRecipeY = 0;
		}
		else
		{
			if (ui.craftingRecipeY >= count)
			{
				ui.craftingRecipeY = (sbyte)(count - 1);
			}
			int count2 = ui.currentRecipeCategory[ui.craftingRecipeY].recipes.Count;
			if (ui.craftingRecipeX >= count2)
			{
				ui.craftingRecipeX = (sbyte)(count2 - 1);
			}
		}
		currentRecipies.Clear();
	}

